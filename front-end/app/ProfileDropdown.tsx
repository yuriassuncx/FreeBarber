'use client';

import { Menu, Transition } from '@headlessui/react'
import { Fragment, useEffect, useState } from 'react'
import { CaretDoubleDown, Bell } from 'phosphor-react'

import { useAuth } from '../hooks/useAuth';
import { getUserById } from '../hooks/get-user-by-id';

import Link from 'next/link';

export function ProfileDropdown() {
    const { user, logoutUser } = useAuth();
    const [schedulesCount, setSchedulesCount] = useState(0);

    async function getUserData() {
      const userData = await getUserById(user?.id);

      setSchedulesCount(userData.Schedules.length);
    }

    useEffect(() => {
      if (user) {
        getUserData();
      }
    }, [user])

  return (
    <div className="hidden md:flex justify-center items-center">
      <Menu as="div" className="hidden absolute md:block text-left bg-indigo-500 hover:bg-indigo-600 rounded-md">
        <div>
          <Menu.Button className="inline-flex w-full justify-center rounded-md bg-black bg-opacity-20 px-4 py-2 text-sm font-medium text-white hover:bg-opacity-30 focus:outline-none focus-visible:ring-2 focus-visible:ring-white focus-visible:ring-opacity-75">
            Perfil
            <CaretDoubleDown
              className="ml-2 -mr-1 h-5 w-5 text-violet-200 hover:text-violet-100"
              aria-hidden="true"
            />
          </Menu.Button>
        </div>
        <Transition
          as={Fragment}
          enter="transition ease-out duration-100"
          enterFrom="transform opacity-0 scale-95"
          enterTo="transform opacity-100 scale-100"
          leave="transition ease-in duration-75"
          leaveFrom="transform opacity-100 scale-100"
          leaveTo="transform opacity-0 scale-95"
        >
          <Menu.Items className="absolute z-[5000] right-0 mt-2 w-56 origin-top-right divide-y divide-gray-100 rounded-md bg-white shadow-lg ring-1 ring-black ring-opacity-5 focus:outline-none">
            <div className="px-1 py-1 ">
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active ? 'bg-violet-500 text-white' : 'text-gray-900'
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                  >
                    {active ? (
                      <EditActiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    ) : (
                      <EditInactiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    )}
                    Minha conta
                  </button>
                )}
              </Menu.Item>
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active ? 'bg-violet-500 text-white' : 'text-gray-900'
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                  >
                    {active ? (
                      <DuplicateActiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    ) : (
                      <DuplicateInactiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    )}
                    Sou barbeiro
                  </button>
                )}
              </Menu.Item>
            </div>
            <div className="px-1 py-1">
              <Menu.Item>
                {({ active }) => (
                  <Link
                    className={`${
                      active ? 'bg-violet-500 text-white' : 'text-gray-900'
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                    href={`schedules`}
                  >
                    {active ? (
                      <ArchiveActiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    ) : (
                      <ArchiveInactiveIcon
                        className="mr-2 h-5 w-5"
                        aria-hidden="true"
                      />
                    )}
                    <p>Meus agendamentos</p>
                    <p className="bg-blue-400 rounded-full mx-2 px-[0.35rem] text-white">{schedulesCount > 0 && schedulesCount}</p>
                  </Link>
                )}
              </Menu.Item>
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active ? 'bg-violet-500 text-white' : 'text-gray-900'
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm before:bg-red-500 before:p-1 before:absolute before:-mt-4 before:rounded-full`}
                  >
                    {active ? (
                      <Bell
                        className="mr-2 h-5 w-5"
                        weight="bold"
                        aria-hidden="true"
                      />
                    ) : (
                      <Bell
                        className="text-[#A78BFA] mr-2 h-5 w-5"
                        weight="bold"
                        aria-hidden="true"
                      />
                    )}
                    Notificações
                  </button>
                )}
              </Menu.Item>
            </div>
            <div className="px-1 py-1">
              <Menu.Item>
                {({ active }) => (
                  <button
                    className={`${
                      active ? 'bg-violet-500 text-white' : 'text-gray-900'
                    } group flex w-full items-center rounded-md px-2 py-2 text-sm`}
                    onClick={logoutUser}
                  >
                    {active ? (
                      <MoveActiveIcon
                            className="mr-2 h-5 w-5"
                            aria-hidden="true"
                        />
                    ) : (
                        <MoveInactiveIcon
                            className="mr-2 h-5 w-5"
                            aria-hidden="true"
                        />
                    )}
                    Deslogar
                  </button>
                )}
              </Menu.Item>
            </div>
          </Menu.Items>
        </Transition>
      </Menu>
    </div>
  )
}

